    public static void main(String[] args) {
        int[] numbers = {9, 8, 7, 6, 5}; // Menggunakan tanda kurung kurawal {}
        int sum = 0;
        for (int i = 0; i < numbers.length; i++){
            sum += numbers[i];
        }
        System.out.println(sum);
    }    
